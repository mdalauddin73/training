<h1 class="text-2xl text-sky-600 font-bold mb-2">Information Technology <span class="text-red-600">Security Considerations:</span></h1>
<p class="font-bold">It is vital that you set a strong password for ALL systems, and that you do not share it with anyone. Never email
anyone your password. Never write down your password. Never tell anyone your password – even if asked.
Change passwords regularly.</p>
<ul class="ml-10 list-disc space-y-3 mt-3">
<li>When not at your laptop, ensure it is locked (Win logo + L).</li>
<li>Even if you know the source, be extra-vigilant when attached files are involved. If you don’t know the source,
never open attachments.</li>
<li>When online, be careful about revealing personal information such as your date of birth, mother's maiden name,
pet's name, etc.</li>
<li>Restrict access to your social networking sites to only people you know. Assume everything you put on such sites
is permanent (forever) and public.</li>
<li>When travelling with a laptop, treat it as you would cash – do not leave it unattended and under no circumstances
should it ever be stored in checked luggage.</li>
<li>If you use a smartphone or tablet to access <span class="text-green-900 font-bold italic">CONCERN</span> email or other <span class="text-green-900 font-bold italic">CONCERN</span> services you MUST pin or password
protect the device. If the device is lost or stolen – even if it is a personal device - you must inform the ICT
department as it must be remotely wiped.</li>
<li>GoogleDrive/DropBox and other cloud-based storage services should not be used for the
exchange of personal or confidential data – please refer to <span class="text-green-900 font-bold italic">CONCERN</span>’s Data Protection Policy for more
information.</li>
</ul>
