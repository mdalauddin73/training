   <p> Enhanced Interior Gateway Protocols</p></br>
   
   <P> EIGRP Named mode</p>
   
   <p> Named EIGRP has three modes. These are:</br></br>

    address-family configuration mode - (config-router-af)#</br>
	configured parameters: Networks, EIGRP neighbor, and EIGRP Router-id.</br></br>
    address-family interface configuration mode - (config-router-af-interface)#</br>
	configured options: EIGRP authentication, split-horizon, and summary-address. </br></br>
    address-family topology configuration mode - (config-router-af-topology)# </br>
	configured options: redistribution, distance, offset list, variance.</br></br></p>

