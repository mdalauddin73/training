<p class="text-2xl">Strong password required</p>
<p class="text-red-600 font-bold italic">'Password must meet complexity requirements'</p>
<p class="text-green-600 font-bold">Strong passwords have the following characteristics:</p>

<ul class="mt-2 ml-5 list-disc">
    <li>Contain both upper-, and lower-case characters (e.g., a—a, A—Z)</li>
    <li>Contain digits and punctuation characters as well as letters
(e.g.,0—9, !@#$%A8CQ_+I-=“ U [1 :“;‘o?,./,</li>
    <li>Are at least eight alphanumeric characters long</li>
    <li>Are not a word in any language, slang, dialect, jargon, and so on</li>
    <li> Are not based on personal information, names of family, and so on</li>
    <li>Are never written down or stored online.</li>
</ul>

